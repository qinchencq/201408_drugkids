<p><b>Psych meds indispensable for some kids</b></p>

<p>Pressure or no, many who are part of the system — while insisting they avoid overmedicating kids — say some use of drugs is essential.</p>

<p>“I wouldn’t want people to think as caregivers we are medicating them so they are comatose, or putting them to sleep so we don’t have to deal with them,” said Barbara Leiner, who fostered more than 300 children in Los Angeles County over 24 years and runs online training forums for foster parents. On the contrary, she said, in her experience medications benefited a significant number of her children.</p>

<p>“They’re better in control of their behaviors. When they’re not on psychotropic drugs, they have a tough time in school, the other kids don’t like them, they’re out of control.”</p>

<p>Psychiatrists concerned about overprescribing acknowledge there are legitimate reasons for some children to take psychotropic medications, and even the riskiest drugs can be lifesaving for the small sliver of kids with psychoses that are clearly diagnosed. Studies show that’s no more than 1 to 2 percent of children, depending on the illness.</p>

<p>Marjorie, an Oakland special-education teacher who uses only her first name, adopted two of her students from the foster care system and said antipsychotics have been indispensable in their care. </p>

<p>The two boys, Jakeel, 18, and Smith, 11, take one antipsychotic medication daily to treat symptoms of severe autism. Off the meds, Jakeel tried to leap out of a moving car because he didn’t get a toy he wanted. Smith killed a family pet and once tried to climb out a third-story window.</p>

<p>“He talks about the ants in his head without the Abilify,” said Marjorie, who approaches her mothering with a cheerful practicality. “So I say, ‘OK, we don’t want ants in your head!’ ” </p>

<p>Still, when her sons first came to live with her, Marjorie quickly discovered they were on more medications while they were in foster care than she felt were necessary. So she lowered the dosages and limited the multiple antipsychotics to just one. </p>
