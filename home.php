<p>A juvenile court authorizes each prescription, but the forms the courts use often lack critical details and a doctor’s expertise is rarely questioned. Nash, the Los Angeles judge, acknowledges the resulting challenges — even in Los Angeles, where mental health experts now review all applications for prescriptions and in 2013 officials created protocols to curb prescribing of multiple psych meds.</p>

<p>“The last time I looked around, there aren’t too many psychiatrists or psychologists on the bench,” the judge said. “So how in the heck are we able to make good decisions about these meds?”</p>

<p><b>How overprescribing happens</b></p>

<p>The progression from medication to overmedication is also surprisingly routine, said child psychiatrist Tony Stanton. </p>

<div class="small-12 hidden-for-small-only large-10 article-img sidebar_tease">
  <a href="#sidebar_2">
    <img src="img/sidebar_2.jpg" />
  </a>
</div>

<p>At first, psych medications can stabilize moods and dangerous behaviors. Yet once the immediate crisis is over, foster children often remain on a high-dose pharmaceutical course that future doctors are reluctant to reverse.</p>

<p>The drugs “might stun them for a while,” said Stanton, who treated Bay Area foster children for 24 years. “But after two or three weeks they’d stop working.” </p>

<p>Then, if the child seems depressed, they add an antidepressant. “And when that got worse, they’d change the diagnosis — they’d say it’s actually bipolar,” Stanton said. “Then, if they said they heard a voice telling them they were bad or something, then they’d say: ‘Oh, they’re psychotic.’ Then an antipsychotic would be added.” </p>

<p>Aggressive behavior? A second antipsychotic may get added to the mix.</p>

<p>“Usually in my report I’d say, if in fact any of this had been successful, the child should not be in our care,” said Stanton, whose San Leandro group home typically received the most difficult-to-place foster children. “So I think we can safely say this does not work.”</p>

<p>Stanton’s reports in recently published articles summing up his work with 450 children ages 5 through 13 are startling: Most children arrived at residential homes run by the Seneca Center for Children and Families on at least four or five — and as many as eight or nine — different psychotropic medications. One 9-year-old shuffled into a Seneca home on a medication dose that was 10 times the amount recommended for a psychotic adult.</p>

<p>“It’s a story I’ve gotten used to,” said Robin Randall, medical director of San Francisco’s Edgewood Center for Children and Families, which also offers residential treatment programs for troubled foster youth. “I used to say when I saw kids walk in on eight or nine different medications that I was shocked and appalled — now I’m just appalled.”</p>

<p>Randall said children “stay on the meds for reasons that are not necessarily heinous. It’s not that doctors want to get kids on a ton of meds. They’re putting out fires, and not allowed the time. The system is set up in a way that everyone is adding, adding, adding, and doesn’t allow for a space to safely take them off.”</p>
                
