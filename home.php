                       <div class="small-12 medium-9 medium-offset-1 large-offset-1 large-9 columns">
<p>And Rochelle Trochtenberg, now 31 and living in Eureka, still struggles to bring a glass to her lips because her hands are so shaky from the years she spent on a shifting mix of lithium, Depakote, Zyprexa, Haldol and Prozac, among others. When people ask, she tries to cover it up with remarks about a possible hereditary condition.</p>

<p>The truth is too painful to explain, she said. “I don’t want to tell people I have a tremor because I was drugged for my whole adolescence.” </p>

<p><b>Questionable prescribing revealed</b></p>

<p>Despite the concerns, state officials have been slow to even reveal foster care prescribing patterns in California. This newspaper and its lawyers spent nine months negotiating with the Department of Health Care Services for data that is public under state and federal law, as long as individuals cannot be identified.</p>

<p>The 10 years of data begins in 2004 and — even though the state continues to resist many of this newspaper’s requests — provides the most comprehensive look yet at psychotropic medication use on California’s foster kids. The newspaper also interviewed more than 175 people, including more than 30 current and former foster youth throughout the state. </p>

<p>The findings, which will be examined here and in future stories, include:</p>

<p><i class="fa fa-square"></i> Growing use of antipsychotics to treat bad behavior: Of the tens of thousands of foster children placed on psychotropic drugs over the past 10 years, nearly 60 percent were prescribed an antipsychotic, the class of psychotropic medications with the highest risks. That figure stunned experts in the field and alarmed officials who oversee the state’s foster care system. The Food and Drug Administration authorizes antipsychotics for children only in cases of severe mental illness, but evidence suggests doctors often prescribe them to California foster children for behavior problems — a legal but controversial practice that critics say should be limited. </p>

<p><i class="fa fa-square"></i> Multiple psych meds common but dangerous: In many cases, doctors piled on prescriptions: 12.2 percent of California foster children who received a psych drug in 2013 were prescribed two, three, four or more psychotropic medications at a time — up from 10.1 percent in 2004. These drug combinations often fall in uncharted medical territory, with no scientific evidence that young brains aren’t being harmed. </p>
