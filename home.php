<p><b>‘It’s the behaviors they want treated’</b></p>

<p>From all quarters, the pressure in favor of the drugs can be intense.</p>

<p>Estrada recalls the consequences at his group home when he refused to take his Seroquel: He couldn’t go on field trips, play video games, watch TV or go outside.</p>

<p>But the pressure starts elsewhere, said longtime public health nurse Carol Brown, with the caregivers struggling to control troubled kids. Many of those caregivers are loving and committed, genuinely trying to do the right thing; others are simply overwhelmed.</p>

<p>“Very often, there’s pressure on the doctors from the foster parents and the group homes to provide medication to deal with the behaviors that the foster youth are exhibiting,” Brown said. “The foster parents won’t take the kids with the behaviors, and it’s the behaviors they want treated.”</p>

<p>Child psychiatrist Edgardo Tolentino, a doctor with Pathways to Wellness, a medication clinic that serves foster children in Alameda and Contra Costa counties, conceded he has felt that same pressure from caregivers.</p>

<p>“The expectation is that they’ll be given some type of medication,” he said. “If they are already on medications, the only thing I can do is continue them.”</p>
