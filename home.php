                  <div class="row foster-article">
                       <div class="small-12 medium-9 medium-offset-1 large-offset-1 large-9 columns">
<p>In a sharp defense of his department’s oversight, Lightbourne insisted those numbers represent progress, even though the percentage of foster children on psych meds has remained roughly the same — ranging from 14 to 16 percent annually — for the past decade.</p>

<p>“Clearly there are some situations in which psychotropic prescriptions may be appropriate,” Lightbourne said. “We have to know that something is being done because it’s absolutely necessary, not because it’s convenient — that it’s not simply behavior management.</p>

<p>“There are things that are much better handled through therapies,” he said. “Psychotropics should be the end, not the start.”</p>

<p>Many on the front lines, however, defend the prescribing, saying the risks are weighed against the benefits and that there are often no alternatives to treating kids with such deep emotional scars. Foster parents and group home directors tend to cast out kids with the most anti-social behaviors, and no one wants them to end up in juvenile halls, psych wards or treatment facilities.</p>

<p>“The goal is to deinstitutionalize the child so he can live safely in the community,” said therapist Randall Ramirez, director of behavioral health for San Jose-based residential treatment provider Unity Care Group. “If one of the drawbacks is they have to be medicated because we don’t want to raise kids in orphanages, that’s the trade-off.”</p>

<p>To be sure, California is not alone in its questionably high prescribing rates. Growing evidence has revealed alarming rates of psychotropic medication use on foster youth across the country, and particularly antipsychotics. The numbers are difficult to compare from state to state because of differences in methodology, but one recent survey found that in 2009, some states, including Texas, Colorado and Missouri, prescribed antipsychotics at an even higher rate than California.</p>

<p><b>Overuse concerns date back decades</b></p>

<div class="small-12 hidden-for-small-only large-10 article-img sidebar_tease">
  <a href="#sidebar_1">
    <img src="img/sidebar_1.jpg" />
  </a>
</div>

<p>The state has known about the overuse of psychotropic medication on foster children for years. Legislators first tried in 1999 to address the concerns, passing a law that made California the only state requiring juvenile courts to approve all psych med prescriptions, with reviews every 180 days.</p>

<p>But this newspaper’s analysis of a decade of prescribing data shows the court oversight has done nothing on a statewide level to lower prescribing rates. A UCLA study in 1998, cited in the legislation, found that 13 percent of school-age foster children in Los Angeles were receiving psychotropic medications. While the state failed to provide data that would allow an updated look at Los Angeles school kids, this newspaper’s study revealed high prescribing rates have persisted statewide for all foster kids and all age groups.</p>                
                     
<p>“I don’t think there’s one substantive thing that we can point to that has ensured that foster children in California today are only receiving psychotropic medications appropriately,” said Bill Grimm, an attorney with the Oakland-based National Center for Youth Law. </p>

<p>Grimm’s organization is a central part of the state effort — along with public health nurses, state pharmacists and child welfare directors — to comply with the federal mandate to better monitor psychotropic medications. But he is frustrated that the group has been meeting for two years without producing any reports or policy recommendations, and said it has been hamstrung in part by the state’s refusal to provide good data on prescribing patterns. </p>

