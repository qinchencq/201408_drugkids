<p><b>‘I wanted to take the pill’</b></p>

<p>When a psychiatrist told Joymara Coleman that medication could help her enjoy life more, “it sounded like magic to me at first, honestly.”</p>

<p>She had been searching for some peace after a decade in and out of foster care in Alameda County.</p>

<p>“It was pretty clear that I was depressed because of all the things that I went through,” said Coleman, now 24 and a senior studying sociology at Cal State East Bay. “I was in the foster care system with folks who weren’t very loving. I had a lot of things I needed to heal from — from losing my mom, losing my siblings, from witnessing my mom smoking crack. I was depressed because my father was incarcerated.”</p>

<p>Coleman had seen her roommate in a home for troubled teens wet the bed while on the medications, and she surely didn’t want that. “But after experiencing so much tragedy and so much confusion and chaos in my life, when the psychiatrist told me that it would make me happy, I wanted to take the pill.” </p>

<p>She was 17 when a doctor prescribed the antidepressant Prozac for depression and panic attacks, but the list of medications grew to a series of overlapping trials of psych meds, according to court records she shared with a reporter: There were antipsychotics Risperdal and Abilify and mood stabilizers such as Depakote and lithium. </p>

<p>Court files show that when Coleman was 18, she “was put on 300 mg of lithium to help her control her anger; however, it was stopped after client felt dizzy and nauseous.” Psychiatrists who reviewed that diagnosis for this newspaper called it disturbing that lithium, a powerful treatment for bipolar disorder, would be prescribed for anger management.</p>

<p>The cocktail of drugs left her exhausted and “spaced out,” dulling her spunky personality. “When I was on the medication I just didn’t act like Joy,” she said.</p>

<p>She said her nurse practitioner seemed to really care about her. But when the self-conscious teen complained about weight gain — she added 35 pounds to her 4-foot-8-inch frame — the nurse simply encouraged her to avoid sugary drinks. </p>

<p>“They were really adamant,” Coleman said of her case workers’ insistence that she take medications. “Initially I bought into it, that I needed this because these are professionals — these are all white professionals, with degrees and they’re older and they’re telling me that something’s wrong with me, and they just know this.”</p>                
