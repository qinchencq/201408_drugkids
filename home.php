<p><b>State says it’s hard at work</b></p>

<p>Officials with the Department of Social Services say they are working hard on improvements. Two years ago, they assembled statewide experts to act on the federal mandate, and they say that group is making good progress.</p>

<p>Lightbourne said the real story behind the numbers is more positive: As the state’s foster care population plunged in the past 10 years — part of a national trend to keep families intact — so has the total number of foster youth prescribed psych medications, dropping by thousands of kids. </p>
