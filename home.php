<p>That kind of attention can be lacking in the foster care system, and that adds another layer of concern for often-displaced kids on such powerful drugs. More than 60 percent of the children who have been in foster care at least two years have moved two or more times in the system.</p>

<p>So even their prescribers — who may be private practitioners or work at public clinics — often know little about them or their family histories when they meet during office visits. Nash recently reviewed 150 requests for medication in the Los Angeles juvenile court and said he found cases in which doctors prescribed with no medical records or drug history.</p>

<p>The dangers are real. For example, some antidepressants carry a “black box label” proclaiming a high suicide risk for young patients. The FDA advises that children be closely monitored for worsening depression and sleeplessness. Yet for foster kids without parents, the medications are often prescribed even when a watchful eye is absent, said attorney Jennifer Rodriguez, a former foster youth who is now executive director of the San Francisco-based Youth Law Center.</p>

<p>“As a parent, when your child goes on something that’s dangerous, the most dangerous drugs that are out there,” she said, “your doctor is relying on you — someone who knows that child, who watches over time.” </p>

<p><b>‘Treatment for a broken heart’</b></p>

<p>Many of the foster youth interviewed for this story shared another theme: There was no need for medication when they got the emotional support they needed all along. Often it was an adult who vowed to stick by them no matter what — a long-lost relative, a teacher, a volunteer advocate, an exceptionally devoted nurse, or the foster parent who seemed to see through the raucous behaviors to the kid inside. </p>

<p>Those key relationships — not medications — are what most helped them eventually calm down and start to feel better, the youth said. </p>

<p>“I cannot count the number of times I have seen children on multiple medications who are really suffering from a broken heart,” said Menlo Park child psychiatrist David Arredondo, who has worked with foster youth for 30 years. “And the treatment for a broken heart is not another medication.”</p>

<p>Today, Rochelle Trochtenberg doesn’t take a single psychotropic medication. She works with troubled youth in Humboldt County and is working on a master’s degree in social work. </p>

<p>But in foster care in Los Angeles, she was diagnosed with a host of mental illnesses, including bipolar, schizoaffective and post-traumatic stress disorders, major depression, bulimia and generalized anxiety.</p>

<p>“They attach all these labels to you in foster care,” she said, “but the bottom line is I come from a home where physical and sexual abuse were my daily norm, where I lived in fear every day, where I felt responsible to protect my younger sister from the abuse.”</p>
