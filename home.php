                  <div class="row foster-article">
                      <div class="small-12 medium-9 medium-offset-1 large-offset-1 large-9 columns">

<p>A year of interviews with foster youth, caregivers, doctors, researchers and legal advocates uncovered how the largest foster care system in the U.S. has grown dependent on quick-fix, taxpayer-funded, big-profit pharmaceuticals — and how the state has done little to stop it.</p>

<p>“To be prescribing these medications so extensively and so, I think, thoughtlessly, with so little evidence supporting their use, it’s just malpractice,” said George Stewart, a Berkeley child psychiatrist who has treated the neediest foster children in the Bay Area for the past four decades. “It really is drugging them.”</p>

<p>The state official who oversees foster care, Department of Social Services Director Will Lightbourne, concedes drugs are overused, but insists his department is wrapping its arms around the problem: “There’s a lot of work to be done here to make sure we do things right.”</p>

<p>No one doubts that foster children generally have greater mental health needs because of the trauma they have suffered, and the temptation for caregivers to fulfill those needs with drugs can be strong. In the short term, psychotropics can calm volatile moods and make aggressive children more docile. </p>

<p>But there is substantial evidence of many of the drugs’ dramatic side effects: rapid-onset obesity, diabetes and a lethargy so profound that foster kids describe dozing through school and much of their young lives. Long-term effects, particularly on children, have received little study, but for some psychotropics there is evidence of persistent tics, increased risk of suicide, even brain shrinkage.</p>

<p>Sade Daniels, of Hayward, became so overweight in her teens, that at age 26 her bathroom mirror still taunts and embarrasses her. Mark Estrada, a 21-year-old from Anaheim, said he felt too “zoned out” to focus on high school and so groggy he was cut from his varsity basketball team.</p>

