<h3 class="sidebar_head" id="sidebar_1"><b>Doctors' dilemma: 'Do benefits outweigh the risks?'</b></h3>

<p>When federal regulators determine that a medication is safe and effective, they assure the public with the FDA stamp of approval on the pill bottle’s label.</p>

<p>But doctors who treat troubled foster children often turn to a legal but worrisome practice: prescribing powerful antipsychotic medications for conditions and age groups no regulator has blessed.</p>

<p>Drug companies are prohibited from promoting their medications for so-called “off-label” uses, but the law allows doctors to prescribe them for conditions that aren’t approved by the Food and Drug Administration. When the patients are children, however, the risks of such prescribing are magnified: Their brains are still developing, and debilitating side effects can have lifelong consequences.</p>

<p>“Whenever you prescribe medication, you have to ask yourself: Do the benefits outweigh the risks?” said George Fouras, a San Francisco child psychiatrist who specializes in foster care. “That always has to weigh heavy on our minds. Do you want to expose somebody to very serious side effects if there are other alternatives?”</p>

<p>Off-label prescribing can be life-saving in some cases, and it is widely used to treat cancer and rare diseases. Fouras said doctors base their decisions to write such prescriptions on their own patients’ outcomes and published reports of case studies. </p>

<p>But one doctor’s comfort level is another’s concern. Menlo Park child psychiatrist David Arredondo said many doctors are too comfortable prescribing antipsychotics to children.</p>

<p>Unless a child has “a major, major psychiatric illness, I don’t think we have any business doing that to a developing child’s brain,” he said. “It’s too risky.” </p>

<p>For children, the FDA has approved antipsychotics only to treat schizophrenia, bipolar disorder and severe autism — serious mental health conditions found in just 1 to 2 percent of the child population. But University of Maryland professor of pharmacy and psychiatry Julie Zito has found that the drugs often are prescribed off-label to control children’s behavior. </p>

<p>In a rare look at diagnoses of children covered by Medi-Cal, California’s public health system, Zito found that almost one-half of the antipsychotics used were off-label and about a third were for behavior problems such as ADHD, “conduct disorder” or “oppositional defiant disorder.” Her yet-to-be-published study found that in 2009 there was an 18-fold greater use of antipsychotics on foster children than on non-foster kids receiving Medi-Cal because of their families’ income levels. These findings were presented to the FDA in April. </p>

<p>There is no research on the effects of psychotropic medication on children’s brains, but there is cause for alarm in the research on adults. In a widely cited study published last year, University of Iowa neuroscientist Nancy Andreasen examined MRI images of 211 schizophrenic adults and determined that chronic use of antipsychotics, not the illness, caused “smaller brain tissue volumes.” </p>

<p>Other side effects of the drugs, such as rapid weight gain and diabetes risk, are well-documented. </p>

<p>In a 2009 study published in JAMA Psychiatry, children on antipsychotics added up to 15 percent of their body weight in less than two weeks. Every one of the study’s 257 participants gained weight. </p>

<p>The studies only verify what doctors such as longtime child psychiatrist Glen Elliott already know. He routinely sees it in his exam room at the Children’s Health Council in Palo Alto. To the doctor’s shock, a new patient, a 5-year-old boy, came to him after ballooning — in six months — from normal weight to morbidly obese. </p>

<p>Elliott said he and other doctors have been stunned to learn how often antipsychotic use in children results in obesity. In his young patient’s case, “the saddest thing was, his behavior was no worse off the medicine than it was on the medicine.”</p>

