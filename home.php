<p><span class="dropcap">T</span> hey are wrenched from abusive homes, uprooted again and again, often with their life’s belongings stuffed into a trash bag.</p>

<p>Abandoned and alone, they are among California’s most powerless children. But instead of providing a stable home and caring family, the state’s foster care system gives them a pill.</p>

<p>With alarming frequency, foster and health care providers are turning to a risky but convenient remedy to control the behavior of thousands of troubled kids: numbing them with psychiatric drugs that are untested on and often not approved for children.</p>

<p>An investigation by this newspaper found that nearly 1 out of every 4 adolescents in California’s foster care system is receiving these drugs — 3 times the rate for all adolescents nationwide. Over the last decade, almost 15 percent of the state’s foster children of all ages were prescribed the medications, known as psychotropics, part of a national treatment trend that is only beginning to receive broad scrutiny.</p>

<p>“We’re experimenting on our children,” said Los Angeles County Judge Michael Nash, who presides over the nation’s largest juvenile court.</p>
