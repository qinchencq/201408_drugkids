                  <div class="small-12 medium-9 medium-offset-1 large-offset-1 large-9 columns">
<p><b>Doctors’ orders rarely questioned</b></p>

<p>A foster child’s path to psych medication can begin innocently enough — for example, when a child can’t sit still in class and receives an attention-deficit diagnosis and a prescription for stimulants. But like so many other painful experiences in these children’s lives, the drugs are often ramped up during a crisis. </p>

<p>Sometimes, kids end up in the hospital after harming themselves or threatening suicide. Often, though, the prescriptions for stronger drugs come after a child lashes out. </p>

<p>In dozens of interviews with this newspaper, foster youth freely recounted their rash behavior, which they attributed to anger and frustration: They broke furniture, punched people or trashed their rooms. Sade Daniels, the Hayward resident, said she threw a chair at a teacher who had deeply humiliated her — and betrayed her confidence — by telling the class she was in foster care.</p>

<p>Mark Estrada, the former Orange County foster youth, depressed and defiant in his late teens after being separated from his siblings, said he had his behavior subdued by the antipsychotic Seroquel, which is approved only for manic episodes associated with bipolar disorder and schizophrenia.</p>

<p>“They’d make me take it anytime I’d have a tantrum or felt rebellious,” Estrada said. “I felt like I was being controlled. They gave it to me in the morning, and I’d fall asleep all day.”</p>
