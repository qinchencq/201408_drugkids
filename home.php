                       <div class="small-12 medium-9 medium-offset-1 large-offset-1 large-9 columns">
<p><i class="fa fa-square"></i> Psych meds the norm in group homes for troubled kids: More than half of the foster kids who live in California’s residential group homes — and as many as 100 percent in some counties with very small numbers in group homes — are authorized by juvenile courts to receive psychotropic drugs. These homes shelter some of the most troubled foster kids, about 3,800 annually, many of whom the system has been unable to place with families. Health care professionals say children are being medicated to sleep to keep them manageable. In these group homes, foster children who refuse medications are often punished, losing basic privileges such as visiting siblings or simply going outdoors.</p>

<p><i class="fa fa-square"></i> Very young kids also medicated: Hundreds of foster children 5 and younger have been prescribed psychotropics, although federal health officials say the drugs are not safe for the very young and other states actively discourage the practice. In California in the last 10 years, an average of at least 275 of these very young children each year have been prescribed psych medications.</p>

<p><i class="fa fa-square"></i> High cost to kids, and taxpayers: California spends more on psych drugs for foster children than on any other kind of medication. This newspaper analyzed Medi-Cal spending on the 10 most costly groups of drugs for foster kids over the last decade. The state shelled out more than $226 million on psych meds for foster children — an astounding 72 percent of the total. </p>

<p><i class="fa fa-square"></i> Illegal marketing drives sales: Company documents show how drug manufacturers misrepresented scientific evidence to maximize the national market for the antipsychotics that are the top five such drugs prescribed to California foster children. The companies eventually agreed to $4.6 billion in settlements with federal prosecutors. Lawsuits revealed that some of the companies’ sales reps pitched doctors to broaden the use of their drugs among children while downplaying side effects, such as massive weight gain and breast growth in boys. Eli Lilly, for example, advised its sales force: “The competition wins if we are distracted into talking about diabetes.” </p>

<p><i class="fa fa-square"></i> State is slow to act: California has done little to address psychotropic drug use among foster children. Three years ago, the federal government called on states to develop plans to monitor the use of psychotropic medications prescribed to foster youth. Many states, including Illinois, Texas and New York, have formal plans. Yet California, with more than 60,000 foster children, has a target date for its policy of no sooner than 2016.</p>
