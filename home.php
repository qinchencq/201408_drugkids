<p>Daniels also looks back on her medication history as a terrible miscalculation. In a six-week span when she was 14, social workers moved her to three different group homes in Alameda County, where she spent much of the time worrying about her stuff being stolen. The antidepressants and antipsychotics used to subdue her during that time didn’t work for what was really hurting, she said.</p>

<p>“When I look back as an adult at who I was when I was initially diagnosed and given the medication — I needed love,” Daniels said. “Nobody really sees that hurt girl, or the one who truly just wants her mom to get her act together and to get off drugs, or who wants a family, something stable.</p>

<p>“The system relies heavily on medication to do a job that parents are supposed to do.”</p>

