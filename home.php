<h3 class="sidebar_head" id="sidebar_2"><b>How we acquired and analyzed a decade of state data</b></h3>

<p>Alarm over psychotropic drug use on foster children has been growing across the country for years, but understanding the scope of the problem in California required months of negotiations with the state just to take a closer look.</p>

<p>The result is an unprecedented review of a decade of prescribing data from the state’s foster care system that begins an ongoing investigation called “Drugging Our Kids.”</p>

<p><b>How we analyzed the problem</b></p>

<p>Foster care prescribing patterns are best tracked by reviewing what the state calls “pharmacy benefit claims” billed to Medi-Cal, the state’s publicly funded health system, which pays drug costs for California foster children. While an individual’s health records are private, much of the information in the pharmacy claims is public under state and federal law. </p>

<p>In October, staff writer Karen de Sá filed a California Public Records Act request for 10 years of those claims, stripped of any information that would identify individuals.</p>

<p><b>What we requested</b></p>

<p>This newspaper sought raw data that would allow it to analyze the number of foster children receiving psychotropic drugs, with breakdowns on age, gender, ethnicity, geography and type of residence. The request also asked for data to provide a window into what experts consider a disturbing trend: the number of children who received multiple psych meds, and the number who received the potent class of drugs known as antipsychotics.</p>

<p><b>The state’s response</b></p>

<p>The state Department of Health Care Services, insisting even “de-identified” data could be used to identify individuals, refused the newspaper’s request for raw data and then twice provided aggregate data that was either incomplete or inaccurate. By late July, after nine months of negotiations, the state delivered aggregate data covering the fiscal years 2004-05 through 2013-14 that allowed for limited analysis. The newspaper’s news research director, Leigh Poitinger, conducted that analysis.</p>

<p>The newspaper and its lawyers continue to fight for parts of the original request.</p>

<p>Other groups that have sought prescribing data from the state have encountered similar resistance. Members of a state task force considering new safeguards for foster children say their work has been hampered because they can’t get data to understand the specific problems.</p>

<p><b>California’s tracking problem </b></p>

<p>The state was able to provide accurate prescription data only for foster children whose health care was through traditional Medi-Cal. But over the last decade, many counties have begun shifting their foster children to managed health care plans. By 2014, about 38 percent of foster children were covered by managed care plans.</p>

<p>Those children are not reflected in the numbers the state provided to the newspaper because the state could not vouch for the accuracy of that data. In reality, there are certainly more foster children receiving psych meds than are reflected in the analysis, so the newspaper used percentages — instead of overall numbers of children — to describe the trends.</p>

<p><b>Why it’s hard to compare studies</b></p>

<p>Other studies on foster youth are limited to children who are in foster care a minimum of six months.</p>

<p>The state used that methodology in a 2010-11 report to the federal government that showed as many as 19.2 percent of California foster children in that year received psychotropic medications. </p>

<p>By comparison, the data the state provided to the newspaper for the same fiscal year showed 14.7 percent of foster children on psych meds.</p>

<p>That’s because that data looked at <i>every</i> foster child covered by Medi-Cal (but not in managed care) over the 10-year period — even ones who were only briefly in foster care.</p>

<p>While the newspaper’s analysis provides a comprehensive view of the state’s prescribing patterns over time, experts say data using this methodology — which was suggested by the state — likely reflects a lower rate of prescribing than is actually occurring.</p>

<p>David Grant is an expert in population health surveys who directs the California Health Interview Survey for the UCLA Center on Health Policy Research. “You may have a lot of people showing up in there,” Grant said, “that never even have a chance to obtain a prescription during their time in foster care.” </p>
